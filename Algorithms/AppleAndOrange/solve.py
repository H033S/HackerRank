def countApplesAndOranges(s, t, a, b, apples, oranges):
    fallen_apples = [1 if s <= a + ap <= t else 0
     for ap in apples]
    fallen_oranges = [1 if s <= b + org <= t else 0
     for org in oranges]

    print(sum(fallen_apples))
    print(sum(fallen_oranges))


